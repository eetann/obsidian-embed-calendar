import fs from "node:fs";
import path from "node:path";
import process from "node:process";
import builtins from "builtin-modules";
import esbuild from "esbuild";
import PostcssPlugin from "./esbuild-postcss.mjs";

const banner = `/*
THIS IS A GENERATED/BUNDLED FILE BY ESBUILD
if you want to view the source, please visit the github repository of this plugin
*/
`;

const prod = process.argv[2] === "production";

const copyFiles = [
	{ from: "dist/main.js", to: "main.js" },
	{ from: "dist/main.css", to: "styles.css" },
	{ from: "manifest.json", to: "manifest.json" },
	{ from: "manifest-beta.json", to: "manifest-beta.json" },
];

const myCopy = {
	name: "my-copy",
	setup(build) {
		build.onEnd(async () => {
			let destination = ".";
			if (!prod) {
				const VAULT_PATH = process.env.VAULT_PATH;
				destination = `${VAULT_PATH}/.obsidian/plugins/obsidian-embed-calendar`;
				console.log(destination);

				if (!fs.existsSync(destination)) {
					fs.mkdirSync(destination);
				}
				fs.writeFileSync(path.join(destination, ".hotreload"), "");
			}
			for (const copyFile of copyFiles) {
				fs.copyFileSync(copyFile.from, path.join(destination, copyFile.to));
			}
		});
	},
};

const context = await esbuild.context({
	banner: {
		js: banner,
	},
	entryPoints: ["src/main.ts"],
	bundle: true,
	external: [
		"obsidian",
		"electron",
		"@codemirror/autocomplete",
		"@codemirror/collab",
		"@codemirror/commands",
		"@codemirror/language",
		"@codemirror/lint",
		"@codemirror/search",
		"@codemirror/state",
		"@codemirror/view",
		"@lezer/common",
		"@lezer/highlight",
		"@lezer/lr",
		...builtins,
	],
	format: "cjs",
	target: "es2018",
	logLevel: "info",
	sourcemap: prod ? false : "inline",
	treeShaking: true,
	outfile: "dist/main.js",
	plugins: [PostcssPlugin({}), myCopy],
});

if (prod) {
	await context.rebuild();
	process.exit(0);
} else {
	await context.watch();
}
